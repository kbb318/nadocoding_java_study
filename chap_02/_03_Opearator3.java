package chap_02;

public class _03_Opearator3 {
    public static void main(String[] args) {
        //비교 연산자
        System.out.println(5 > 3); //참이면 true
        System.out.println(5 >=3 );
        System.out.println(5 >=5 );
        System.out.println(5 >=7);

        System.out.println(5 < 3);
        System.out.println(5 <= 3);

        System.out.println(5 ==5 );

        System.out.println(5 != 5);
        System.out.println(5 != 3);
    }
}
